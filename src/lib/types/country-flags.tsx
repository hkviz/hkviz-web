export function countryFlag(country: CountryCode): string | null {
	return flagByCode[country] ?? null;
}

export function CountryFlag(props: { code: CountryCode }) {
	const flag = () => countryFlag(props.code)?.replace('<svg', '<svg class="w-4 h-[0.666rem]"');
	return (
		<Show when={flag()}>
			{(flag) => (
				<div
					// eslint-disable-next-line solid/no-innerhtml
					innerHTML={flag()}
					class="mr-2 border border-black border-opacity-20 dark:border-white dark:border-opacity-20"
				/>
			)}
		</Show>
	);
}
